const translations = {
  vi: viTranslations,
  en: enTranslations
};

let currentLanguage: Language = "vi";

type I18nContextValue = {
  language: Language;
  loading: boolean;
  setLanguage: (language: Language) => Promise<void>;
  t: (key: TranslationKey, values?: TranslationValues) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

function isLanguage(value: string | null): value is Language {
  return value === "vi" || value === "en";
}

function interpolate(template: string, values?: TranslationValues) {
  if (!values) {
    return template;
  }

  return template.replace(/\{\{(\w+)\}\}/g, (match, key: string) => {
    const value = values[key];
    return value === undefined ? match : String(value);
  });
}

export function translate(
  key: TranslationKey,
  values?: TranslationValues,
  language: Language = currentLanguage
) {
  const template = translations[language][key] ?? translations.vi[key] ?? key;
  return interpolate(template, values);
}

export function getCurrentLanguage() {
  return currentLanguage;
}

export function LanguageProvider({ children }: PropsWithChildren) {
  const [language, setLanguageState] = useState<Language>(currentLanguage);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    AsyncStorage.getItem(STORAGE_KEYS.language)
      .then((storedLanguage) => {
        if (isLanguage(storedLanguage)) {
          currentLanguage = storedLanguage;
          setLanguageState(storedLanguage);
        }
      })
      .finally(() => setLoading(false));
  }, []);

  const setLanguage = useCallback(async (nextLanguage: Language) => {
    currentLanguage = nextLanguage;
    setLanguageState(nextLanguage);
    await AsyncStorage.setItem(STORAGE_KEYS.language, nextLanguage);
  }, []);

  const t = useCallback(
    (key: TranslationKey, values?: TranslationValues) =>
      translate(key, values, language),
    [language]
  );

  const value = useMemo(
    () => ({
      language,
      loading,
      setLanguage,
      t
    }),
    [language, loading, setLanguage, t]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const value = useContext(I18nContext);
  if (!value) {
    throw new Error("useI18n must be used inside LanguageProvider");
  }
  return value;
}
